# Listas - Métodos e atributos

listaPaises = ['Brasil', 'Itália', 'França', 'Portugal']

listaPaises.append('China')
print(listaPaises)

listaPaises.remove(listaPaises[3])
print(listaPaises)

listaPaises.remove('França')
print(listaPaises)

listaPaises.pop(1)
print(listaPaises)

listaPaises.clear()
print(listaPaises)

listaPaises = ['Brasil', 'Itália', 'França', 'Portugal']
listaPaises.reverse()
print(listaPaises)

print(listaPaises.pop(2))
print(listaPaises)

listaPaises = ['Brasil', 'Itália', 'França', 'Portugal', 'China']
listaPaises.sort()
print(listaPaises)
